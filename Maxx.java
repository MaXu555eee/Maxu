public class Maxx {
	
	public static void main(String[] args) {
		int a = 7;
		int b = 10;
		int c = 268;
		int d = 2;
		System.out.println("a + b =" + (a + b));
		System.out.println("a - b =" + (a - b));
		System.out.println("a * b =" + (a * b));
		System.out.println("b / a =" + (b / a));
		System.out.println("a + c =" + (a + c));
		System.out.println("c + b =" + (c + b));
		System.out.println("++d =" + (++d));
		System.out.println("--d =" + (--d));
		System.out.println("b % a =" + (b % a));
	}
}